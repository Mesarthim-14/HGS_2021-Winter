//=============================================================================
// コンストラクタ
//=============================================================================
CJudge::CJudge(PRIORITY Priority) : CScene(Priority)
{
    m_Hand = HAND_TYPE_NONE;
    m_pScene2D = nullptr;
}

//=============================================================================
// デストラクタ
//=============================================================================
CJudge::~CJudge()
{
}

//=============================================================================
// インスタンス生成
//=============================================================================
CJudge * CJudge::Create()
{
    // メモリ確保
    CJudge *pTestModel = new CJudge(PRIORITY_1);

    // !nullcheck
    if (pTestModel)
    {
        // 初期化処理
        pTestModel->Init();

        return pTestModel;
    }

    return nullptr;
}

//=============================================================================
// 初期化処理
//=============================================================================
HRESULT CJudge::Init()
{

    return S_OK;
}

//=============================================================================
// 終了処理
//=============================================================================
void CJudge::Uninit()
{
    if (m_pScene2D)
    {
        m_pScene2D->Uninit();
        m_pScene2D = nullptr;
    }

    Release();
}

//=============================================================================
// 更新処理
//=============================================================================
void CJudge::Update()
{

}

//=============================================================================
// 画像
//=============================================================================
void CJudge::BindScene2D(CScene2D* pScene)
{
    if (m_pScene2D)
    {
        m_pScene2D->Uninit();
        m_pScene2D = nullptr;
    }

    m_pScene2D = pScene;

}